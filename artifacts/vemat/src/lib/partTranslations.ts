function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

/** Strip accents/diacritics for matching (so "VERIN" matches "Vérin"). */
function stripAccents(s: string): string {
  return s.normalize("NFD").replace(/[̀-ͯ]/g, "");
}

// Pre-compiled — sorted by length desc so longest phrases match first.
// Each pattern uses the accent-stripped FR phrase as the regex source so it
// matches any combination of accents/casing in the input.
const COMPILED: Array<[RegExp, string]> = [...PHRASES]
  .sort((a, b) => b[0].length - a[0].length)
  .map(([fr, en]) => [
    new RegExp("\\b" + escapeRegex(stripAccents(fr)) + "\\b", "gi"),
    en,
  ]);

/**
 * Translate French part designations to English. Already-English text passes
 * through unchanged. The matcher is accent-insensitive (matches "VERIN" ↔
 * "Vérin") and case-insensitive.
 *
 * Implementation: we strip accents from the input only for the matching pass,
 * then map matched ranges back onto the original string when replacing — so
 * the non-translated parts keep their original accents/case.
 */
export function translatePartFrToEn(text: string): string {
  if (!text) return "";
  let out = text;
  // Apply each pattern. We work on a parallel "ascii" view to find matches,
  // but emit replacements directly into the original-position string. To do
  // that safely we use replaceAll-style with a callback that replaces the
  // matched slice (in original) with the EN value.
  for (const [re, en] of COMPILED) {
    // Build a transient view: same length as out, no accents — so indices align.
    const ascii = stripAccents(out);
    let result = "";
    let last = 0;
    re.lastIndex = 0;
    let m: RegExpExecArray | null;
    while ((m = re.exec(ascii)) !== null) {
      result += out.slice(last, m.index);
      result += en;
      last = m.index + m[0].length;
      if (re.lastIndex === m.index) re.lastIndex++; // safety against zero-width
    }
    result += out.slice(last);
    out = result;
  }
  return out;
}
